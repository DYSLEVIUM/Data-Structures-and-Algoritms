class Solution {
public:
    int bulbSwitch(int n) {
        // bulbs will be on, if d divides i an odd number of times
        // divisors come in pairs, so we will always have an even number of divisors except when x is perfect square, so we need to count the number of perfect squares from 1 to n which is sqrt(n)
        return sqrt(n);
    }
};
